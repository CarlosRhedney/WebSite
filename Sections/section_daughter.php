              <h2 align="center"><span style="font-family: 'Shadows Into Light', cursive; font-size:40px; color:#9400D3;">Pearl Jam</span></h2>
              <div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
                <div class="panel panel-default">
                  <div class="panel-heading" style="background:#FF0000" role="tab" id="headingOne">
                    <h4 class="panel-title" align="center">
                      <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
                       <span style="color:#fff">Pearl Jam</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
                     </a>
                   </h4>
                 </div>
                 <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingOne">
                  <div class="panel-body">
                    <p>
                      Pearl Jam é uma banda norte-americana de rock alternativo, formada no ano de 1990 em Seattle, Washington. Desde sua origem, é formada por Eddie Vedder, Jeff Ament, Stone Gossard e Mike McCready, passando por mudanças na bateria, sendo Matt Cameron, que também compõe o Soundgarden, o atual baterista da banda. O Allmusic se refere ao Pearl Jam como "a banda americana de rock &amp; roll mais popular dos anos 90." O Pearl Jam foi incluído no Rock and Roll Hall of Fame em 2017.
                      Formada após a dissolução da Mother Love Bone, banda anterior de Ament e Gossard, o Pearl Jam estourou no mainstream com seu primeiro álbum, Ten. Durante a carreira, seus membros se tornaram notados pela recusa em aderir às tradicionais práticas da indústria musical, incluindo a recusa em produzir videoclipes e o engajamento em um boicote contra a Ticketmaster. Em 2006, a Rolling Stone descreveu a banda como tendo "gastado muito da última década deliberadamente tentando destruir sua própria fama."
                      A banda já vendeu mais de 30 milhões de álbuns somente nos Estados Unidos, e aproximadamente 60 milhões em todo o mundo. É considerada uma das bandas mais importantes da década de 1990.
                    </p>
                  </div>
                </div>
              </div>
              <div class="panel panel-default">
                <div class="panel-heading" style="background:#FF0000" role="tab" id="headingTwo">
                  <h4 class="panel-title" align="center">
                    <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                      <span style="color:#fff">Formação: 1984–1990</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
                    </a>
                  </h4>
                </div>
                <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
                  <div class="panel-body">
                    <p>
                      Stone Gossard e Jeff Ament eram, no meio dos anos 80, membros da Green River, uma das bandas pioneiras do movimento grunge. Com essa banda, eles gravaram e se apresentaram até atingirem um sucesso moderado, mas a banda foi dissolvida em 1987, em razão de conflitos de estilo entre os dois e os demais integrantes — Mark Arm e Steve Turner, que viriam a formar o Mudhoney. Gossard e Ament, então, começaram a tocar com Andrew Wood, vocalista do Malfunkshun, algo que viria a se tornar a banda Mother Love Bone. Em 1988 e 1989, o conjunto gravou algumas músicas e fez alguns shows, atraindo um interesse crescente pela sua música; no começo de 1989, a banda conseguiu apoio da gravadora PolyGram, que assinou com eles. Apple, álbum debut do Mother Love Bone, foi lançado em julho de 1990, quatro meses após Wood ter morrido, vítima de uma overdose de heroína.
                      Devastados com a morte de Wood, Gossard e Ament encerraram o Mother Love Bone. Gossard, por sua vez, passou a trabalhar em um material mais pesado, mais "afiado" em relação ao que vinha fazendo antes. Depois de alguns meses ele começou a praticar com seu amigo guitarrista Mike McCready, cuja banda, Shadow, havia acabado; McCready, por sua vez, encorajou Gossard a se reestabelecer com Ament. Após ensaiarem por um tempo, o trio lançou uma fita demo, buscando encontrar um vocalista e um baterista. Eles deram esta fita à Jack Irons, ex-baterista do Red Hot Chili Peppers, para ver se ele estaria interessado em unir-se à banda e, também, para que a distribuísse, buscando encontrar um vocalista.
                      Irons rejeitou o convite, mas enviou o demo a um amigo seu de San Diego chamado Eddie Vedder. Vedder fora o vocalista de uma banda local chamada Bad Radio, trabalhando como segurança durante o dia. Ele ouvia a fita antes de ir surfar, enquanto as letras vinham à sua cabeça. Ele, então, gravou os vocais de três das cinco músicas ("Alive", "Once" e "Footsteps"), algo que ele descreveu como uma "mini-ópera" intitulada Mamasan. Vedder enviou a fita com seus vocais para os três músicos em Seattle; eles ficaram tão impressionados que fizeram Vedder voar para Seattle para uma audição. Em uma semana, Vedder havia se juntado à banda.
                      No meio de 1990, Chris Cornell abordou Gossard e Ament com duas músicas que ele havia escrito em tributo à Andrew Wood, com a intenção de lançá-las como single. Ament descreveu a colaboração como "uma coisa muito boa à época" para ele e Gossard, que colocou-os numa "situação onde eles podiam tocar e fazer música". A formação desse conjunto foi completada com a adição de Matt Cameron, baterista do Soundgarden (e, futuramente, do Pearl Jam), Mike McCready (futuro guitarrista solo do Pearl Jam) e Eddie Vedder fazendo vocais de apoio em diversas canções, assim como um dueto com Chris Cornell em "Hunger Strike". Eles se intitularam Temple of the Dog, uma referência a uma linha de "Man of Golden Words", do Mother Love Bone. As duas canções propostas por Cornell seriam, eventualmente, trabalhadas no primeiro e único álbum da banda, Temple of the Dog, lançado no fim de 1990.
                      Com o reforço de Dave Krusen na bateria, a banda assumiu o nome de Mookie Blaylock, em referência ao então jogador de basquete. A primeira apresentação oficial desse conjunto foi no café Off Ramp, em Seattle, em 22 de outubro de 1990 e, logo, o grupo assinou com a Epic Records. Todavia, preocupações com questões de marca registrada tornaram necessária uma mudança no nome; o "Mookie Blaylock", então, passou a se chamar "Pearl Jam". Em uma entrevista promocional, Vedder disse que o nome "Pearl Jam" era uma referência à sua bisavó Pearl que era casada com um nativo americano e tinha uma receita especial para geléia de peiote. Em uma entrevista para a revista Rolling Stone, em 2006, ele admitiu que aquela história era uma "mentira total", ainda que, de fato, sua bisavó se chamasse Pearl. Ament e McCready explicaram que Ament viera com "pearl", tendo a banda completado com "Pearl Jam", após assistirem a um show de Neil Young, no qual ele estendeu suas músicas com improvisações de 15 a 20 minutos, uma prática conhecida como jamming.
                    </p>
