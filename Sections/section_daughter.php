                    <p>
                      Ao longo de sua carreira, o Pearl Jam lançou onze álbuns de estúdio, 31 singles, duas compilações, sete álbuns ao vivo e inúmeros bootlegs, como parte do programa da banda de bootlegs oficiais, que consiste em gravar profissionalmente seus shows e lançá-los.
                      Afora seus lançamentos oficiais, o Pearl Jam coleciona algumas músicas gravadas para outras ocasiões, além de canções disponíveis em trilhas sonoras; em 1992, por exemplo, a banda gravou "Breath" e "State of Love and Trust" para o filme Vida de Solteiro, de Cameron Crowe. Outras músicas recorrentes em apresentações da banda são "I Believe in Miracles", dos Ramones (presente, inclusive, no álbum Live at the Gorge 05/06), "Rockin' in the Free World", de Neil Young, e versões de músicas do The Who, como "Baba O'Riley", "The Kids Are Alright" e "Love, Reign O'er Me", gravada para o filme Reign Over Me, de Mike Binder. Outro exemplo é a versão cover feita para "Last Kiss", de Wayne Cochran; a canção, inicialmente lançada como single exclusivo para o fã-clube da banda, teve de ser lançada ao público em geral devido à demanda, sendo, até hoje, o single do Pearl Jam a atingir a posição mais alta em uma parada musical — atingiu a segunda posição da Billboard e a primeira posição na ARIA.
