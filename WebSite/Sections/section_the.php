                <div class="panel-heading" style="background:#FF0000" role="tab" id="headingFor">
                  <h4 class="panel-title" align="center">
                    <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseFor" aria-expanded="false" aria-controls="collapseFor">
                      <span style="color:#fff">Discografia</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
