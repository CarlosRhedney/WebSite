                <div id="collapseFor" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFor">
                  <div class="panel-body">
                    <p>
                      O Nirvana lançou apenas três álbuns de estúdio, Bleach, Nevermind e In Utero acompanhados de dois EPs, Blew e Hormoaning. Após a morte de Cobain, o Nirvana lançou três álbuns ao vivo, sendo um acústico, o MTV Unplugged in New York. A banda lançou várias coletâneas e box sets como Incesticide, Nirvana, With the Lights Out, entre outras. O último lançamento póstumo do Nirvana foi uma coletânea chamada Icon.
                      A banda possui vinte e um singles, com os singles de maior sucesso sendo "Love Buzz (1988)", "About a Girl (1994)", "Smells Like Teen Spirit (1991)", que foi a canção de maior sucesso da história da banda, "Come as You Are (1992)", "Lithium (1992)", "In Bloom (1992)", "Heart-Shaped Box (1993)", "All Apologies (1993)'', ''Rape Me (1993)", "Pennyroyal Tea (1994)" e "You Know You're Right", lançada após a morte de Cobain, em 2002.<br />
                      <br /><strong>Aclamação e reconhecimento das canções</strong><br />
                      As canções do Nirvana receberam em sua maioria, aclamação da crítica e tiveram um bom desempenho nas paradas pelo mundo. "Smells Like Teen Spirit" foi um sucesso e recebeu a aclamação da crítica. A imprensa musical atribuiu à canção o estatuto de "o hino de uma geração", colocando Cobain como um relutante porta-voz da Geração X. Em 2000, a MTV e a Rolling Stone colocaram a canção no 3º lugar da lista das "100 Melhores Canções Pop", apenas atrás da "Yesterday" dos Beatles e da "(I Can't Get No) Satisfaction" dos The Rolling Stones, além de entrar em várias listas de melhores canções e listas de fim de ano feitas pela Rolling Stone, Melody Maker, The Village Voice, entre várias outras. Também foi listada pela Rolling Stone em 2004 no 9º lugar das "500 Maiores Canções de Todos os Tempos" e listada no mesmo ano pela Uncut como a maior canção do Nirvana. A canção ficou em 1º lugar na parada Billboard Modern Rock Tracks nos Estados Unidos e nas paradas da França, entre outros países, e foi o único single da banda a ser certificado pela RIAA. "Come as You Are" foi listada pela Kerrang! como um dos "100 Maiores Singles de Todos os Tempos" ficando em 28º lugar. Ela alcançou o 3º lugar na parada Billboard Mainstream Rock Tracks nos Estados Unidos. "Heart-Shaped Box" e "All Apologies/Rape Me" alcançaram a 4ª posição na Billboard Mainstream Rock Tracks, e também alcançaram a 1ª posição na Billboard Modern Rock Tracks. "Heart-Shaped Box" foi listada na posição 116 das "1001 Melhores Canções de Sempre" e como a 4ª maior canção do Nirvana pela Uncut, enquanto que "All Apologies" ficou na 3ª e "Rape Me" na 10ª posição das "20 Maiores Canções do Nirvana". "All Apologies" foi listada como 455ª canção na lista das "500 Maiores Canções de Todos os Tempos" pela Rolling Stone.
                      "About a Girl", que foi lançada inicialmente em Bleach, se tornou um single do álbum acústico MTV Unplugged in New York, fazendo muito sucesso e alcançando a 1ª posição na Billboard Modern Rock Tracks e a 3ª posição na Billboard Mainstream Rock Tracks, e sendo considerada a 2ª maior canção da banda, de acordo com a Uncut. "You Know You're Right", o último single da banda, lançado após vários anos da morte de Kurt Cobain e também a última canção gravada pela banda, alcançou as primeiras posições das paradas Billboard Mainstream Rock Tracks e Billboard Modern Rock Tracks, sendo considerada um dos singles do ano pelo Allmusic.
                    </p>
