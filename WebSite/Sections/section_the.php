                <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
                  <div class="panel-body">
                    <p>
                      Kurt Cobain e Krist Novoselic conheceram-se quando estudavam na Aberdeen High, apenas de vista, de acordo com Cobain. Os dois acabaram por se tornar amigos enquanto frequentavam ensaios do Melvins (banda muito conhecida no cenário local). Cobain queria formar uma banda com Novoselic, mas Novoselic não respondia à suas propostas, que incluía entregar-lhe uma fita demo do projeto Fecal Matter. Três anos depois dos dois se encontrarem pela primeira vez, Novoselic notifica Cobain que ele tinha ouvido finalmente a demo do Fecal Matter que Cobain lhe tinha dado, e sugere que eles comecem uma banda. Os dois recrutam Bob McFadden na bateria, mas depois de um mês, o projeto desfaz-se. No inverno de 1987, Cobain e Novoselic recrutam o baterista Aaron Burckhard. O trio ensaiava o material da demo do Fecal Matter de Cobain, mas começaram a escrever o novo material logo após a formação.
                      Durante os primeiros meses, a banda passou por uma série de nomes, começando como Skid Row e depois Pen Cap Chew, Bliss e Ted Ed Fred. O grupo finalmente estabeleceu-se como Nirvana, que Cobain disse que foi escolhido porque "eu queria um nome que fosse uma espécie de bonito ou agradável e bonito em vez de um nome punk rock mesquinho, obsceno como Angry Samoans". Com Novoselic e Cobain tendo-se mudado para Tacoma e Olympia, respectivamente, os dois perderam contacto temporariamente com Burckhard. Os dois, como alternativa, ensaiavam com Dale Crover do Melvins, e Nirvana gravava as suas primeiras demos em janeiro de 1988. No início de 1988, Crover muda-se para São Francisco mas recomenda Dave Foster para a banda como seu substituto na bateria. A ocupação de Foster com o Nirvana durou apenas alguns meses; durante um breve período na prisão, ele foi substituído pelo retorno de Burckhard, que também não permaneceu na banda depois de contar a Cobain que estava de ressaca para ensaiar num dia. Cobain e Novoselic colocaram um anúncio na publicação musical de Seattle The Rocket procurando por um baterista substituto no qual só conseguiram respostas insatisfatórias. Enquanto isso, um amigo em comum apresentou-os Chad Channing, e os três músicos concordaram em tocar juntos. Channing continuava a tocar com Cobain e Novoselic, embora o baterista tivesse comentado: "Eles nunca realmente disseram: 'Ok, você está dentro.'", e ele fazia o seu primeiro show com a banda em maio.
                    </p>
