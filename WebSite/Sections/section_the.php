              <h2 align="center"><span style="font-family: 'Shadows Into Light', cursive; font-size:40px; color:#9400D3;">Nirvana</span></h2>
              <div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
                <div class="panel panel-default">
                  <div class="panel-heading" style="background:#FF0000" role="tab" id="headingOne">
                    <h4 class="panel-title" align="center">
                      <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
                       <span style="color:#fff">Nirvana</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
                     </a>
                   </h4>
                 </div>
                 <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingOne">
                  <div class="panel-body">
                    <p>
                      Nirvana foi uma banda norte-americana de rock, formada pelo vocalista e guitarrista Kurt Cobain e pelo baixista Krist Novoselic em Aberdeen no ano de 1987, que obteve grande sucesso em meio ao movimento grunge de Seattle no início dos anos 90. Vários bateristas passaram pelo Nirvana, sendo o que ficou mais tempo na banda foi Dave Grohl, que entrou em 1990.
                      No final da década de 1980 o Nirvana se estabeleceu como parte da cena grunge de Seattle, lançando seu primeiro álbum, Bleach, pela gravadora independente Sub Pop em 1989. A banda desenvolveu um som que se baseava em contrastes dinâmicos, muitas vezes entre versos calmos e barulhentos, e refrões pesados. Depois de assinar com a gravadora DGC Records, o grupo encontrou o sucesso inesperado com Smells Like Teen Spirit, o primeiro single do segundo álbum da banda, Nevermind (1991). O sucesso repentino da banda amplamente popularizou o rock alternativo como um todo, e como o vocalista da banda, Cobain se encontrou referido na mídia como o "porta-voz de uma geração", com o Nirvana sendo considerado a "principal banda" da Geração X. Nevermind é citado como um dos maiores álbuns de todos os tempos, e contém 3 singles na lista de  "500 Maiores Canções de Todos os Tempos", da revista Rolling Stone. O terceiro álbum de estúdio do Nirvana, In Utero (1993), desafiou a audiência do grupo, apresentando um som abrasivo, natural e cru, menos mainstream. In Utero, apesar de ser um álbum que se volta contra o sistema (fama e mídia), também foi muito bem sucedido, surpreendendo a crítica, os produtores, e até mesmo a própria gravadora.
                      A breve duração do Nirvana terminou após a morte de Cobain em 1994, mas vários lançamentos póstumos têm sido emitidos desde então, supervisionados por Novoselic, Dave Grohl e pela viúva de Cobain, Courtney Love. Apesar de ter lançado apenas 3 álbuns, a banda, desde o disco de estreia, já vendeu mais de 75 milhões de cópias em todo o mundo. Destas, 25 milhões foram vendidas apenas nos Estados Unidos.
                    </p>
                  </div>
                </div>
              </div>
