              <h2 align="center"><span style="font-family: 'Shadows Into Light', cursive; font-size:40px; color:#9400D3;">Red Hot Chili Peppers</span></h2>
              <div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
                <div class="panel panel-default">
                  <div class="panel-heading" style="background:#FF0000" role="tab" id="headingOne">
                    <h4 class="panel-title" align="center">
                      <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
                       <span style="color:#fff">1983–1984: Formação e primeiro álbum</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
                     </a>
                   </h4>
                 </div>
                 <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingOne">
                  <div class="panel-body">
                    <p>
                      O Red Hot Chili Peppers tem seu embrião e seu feto desde 1979, nos intervalos entre aulas da Fairfax High School, em Hollywood, Los Angeles. Os garotos de 15 anos, Flea, Hillel Slovak e Jack Irons eram três amigos que tinham algumas ambições musicais formando a banda chamada Anthym. Um dos grandes admiradores dessa banda era Anthony Kiedis, também amigo de infância de Flea, Hillel e Irons.[1]
                      Em fevereiro de 1983 nasce o Red Hot Chili Peppers, ainda com o nome de "Tony Flow and the Miraculously Majestic Masters of Mayhem", a partir de uma ideia súbita de Anthony Kiedis, e com ele Flea, Hillel Slovak, Jack Irons e se apresentam pela primeira vez no Rhythm Lounge, um clube de Los Angeles para um público de 30 pessoas. Eles "escreveram" para a ocasião, que envolveu a banda improvisando uma canção, enquanto Kiedis fez um rap de um poema que ele havia escrito, chamado "Out in LA".[5] Slovak e Irons já estavam comprometidos com outro grupo, What Is This?, que se destinava a ser uma performance única. No entanto, o desempenho foi tão animado que a banda foi convidada a voltar na semana seguinte.[6] Devido a este sucesso inesperado, a banda mudou seu nome para The Red Hot Chili Peppers, tocando vários shows em vários clubes de Los Angeles e locais de musicais . A explicação para o nome vem de um improviso de Anthony Kiedis que conta: "Eu estava caminhando em Hollywood Hills e vi esse nome piscando num arbusto psicodélico" (o arbusto era em formato de pimenta), mas essa história parece muito mais uma lenda ou piada de Anthony, do que a verdadeira história. O que parece ser mais próximo da realidade é que eles adoravam comida mexicana com bastante pimenta (chili), Flea era fã da banda de apoio de Louis Armstrong "Red Hot Peppers" e Anthony Kiedis não poderia esquecer o nome da banda que tocou num pub em Londres, a "Chili Willy and the Red Hot Peppers". Seis canções desses shows iniciais foram na fita da banda primeira demo, que os tornaram famosos.
                    </p>
                  </div>
                </div>
              </div>
              <div class="panel panel-default">
                <div class="panel-heading" style="background:#FF0000" role="tab" id="headingTwo">
                  <h4 class="panel-title" align="center">
                    <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                      <span style="color:#fff">2001-2004: By the Way, Live at Slane Castle e Greatest Hits</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
                    </a>
                  </h4>
                </div>
                <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
                  <div class="panel-body">
                    <p>
                      Em julho de 2002, a banda lança o seu oitavo disco da carreira, intitulado By the Way. Todos os integrantes se livraram de vez das drogas. E assim continuava uma série de sucessos, mesmo sem as drogas. O álbum vendeu mais de um milhão de cópias só na primeira semana do lançamento, recebendo muitas críticas dos fãs, pois praticamente só haviam baladas românticas. Muito provavelmente por ter sido o primeiro álbum em anos que a banda fez sem usar nenhum tipo de droga. Mas, mesmo deixando o funk rock um pouco apagado, o álbum tinha algumas canções de destaque, como "Dosed", "The Zephyr Song", "By the Way" e "Can't Stop". Também foi nesse álbum que surgiu uma das primeiras críticas à mídia vinda dos Peppers, com a canção "Throw Away Your Television" (Jogue fora sua televisão).
                      Em novembro de 2003, a banda lança o Greatest Hits, álbum com algumas das músicas que fizeram mais sucesso durante toda a carreira dos Chili Peppers. Teve também duas músicas inéditas: "Fortune Faded" e "Save the Population". Dentre as faixas do álbum By the Way, as canções "Universally Speaking" e "By the Way" foram inclusos na coletânea. Também nesse mesmo ano, a banda faz um show que virou DVD, o Live at Slane Castle. O show foi na Irlanda, em 23 de agosto de 2003.
                      Em 2004, a banda lança seu primeiro álbum ao vivo, o Live in Hyde Park, na Inglaterra, no qual foi feito dois CDs do show. Também foram tocadas duas músicas inéditas da banda; "Rolling Sly Stone" e "Leverage of Space". Em 2005, a banda faz poucos shows, pois estavam todos cansados com a turnê mundial do álbum By the Way. Nesse mesmo ano, a banda participou de um evento beneficente no "React Now" no dia 9 de setembro de 2005, em Hollywood, Califórnia.
                      2005–2007: Stadium Arcadium
                      Em 2006 foi lançado o álbum duplo Stadium Arcadium, nono álbum de estúdio da banda, lançado em 5 de maio através da Warner Bros. Records. O álbum vendeu 442 mil cópias nos Estados Unidos na primeira semana e estreou em primeiro lugar na Billboard 200. Os singles "Snow ((Hey Oh))", "Tell Me Baby" e "Dani California" emplacam. O álbum duplo possui dois CDs intitulados "Mars" e "Júpiter". Destaque para "Dani California" que é a primeira canção do álbum duplo. Planejado inicialmente para ser lançado como uma trilogia em partes separadas, o álbum, duplo, compila as 28 melhores músicas gravadas pela banda em parceria com o produtor Rick Rubin. Com mais de 13 milhões de cópias comercializadas ao redor do mundo, o álbum se tornou o mais vendido de 2006. No clipe de "Dani California" algumas bandas são homenageadas, como Sex Pistols, Nirvana e The Beatles. Dani California é uma personagem da cabeça de Anthony. Ela também aparece em outras duas músicas da banda; "By the Way"e "Californication".
                      Em fevereiro de 2007, a banda concorreu na 49.ª edição do Grammy Awards em Staples Center, Los Angeles. O álbum Stadium Arcadium foi indicado em sete categorias e ganhou quatro grandes prêmios: "Melhor Performance de Rock em Dupla ou Grupo" (Dani California), "Melhor Música Rock" (Dani California), "Melhor Álbum de Rock" (Stadium Arcadium), "Melhor Disco em Edição Especial (Box)" (Stadium Arcadium),[2] além de "Produtor do Ano" (Rick Rubin).
                    </p>
