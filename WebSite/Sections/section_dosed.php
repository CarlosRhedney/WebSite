                <div class="panel-heading" style="background:#FF0000" role="tab" id="headingThree">
                  <h4 class="panel-title" align="center">
                    <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                      <span style="color:#fff">Legado e Reconhecimentos</span><span style="color:#fff" class="glyphicon glyphicon-chevron-right"></span>
                    </a>
                  </h4>
                </div>
                <div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree">
                  <div class="panel-body">
                    <p>
                      A banda é considerada de longe a mais influente do Funk rock e Rock alternativo. É conhecida por valorizar muito o baixista Flea, que é, em algumas músicas, mais ouvido do que a guitarra. Os riffs marcantes e pesados de baixo são de grande autoria do RHCP. Críticos musicais consideram o som dos Peppers muito influente e revolucionário na cultura popular dos anos 90 e 2000, por tornar um som funk, da periferia dos Estados Unidos, ser inserida, e até hoje não sair do cenário pop e mainstream. Com uma base de fãs grande e fiel, vem renovando o estilo de sua música com o passar das gerações, nunca saindo da moda. Por isso é considerada como uma das maiores bandas da história do rock e da música popular em geral, sempre gerando lucros onde tocam e lotando estádios e festivais musicais, como no Rock in Rio em 2001, atraindo cerca de 250 mil pessoas num único dia, o maior público da história do festival. Os Peppers contribuíram muito para o sucesso da MTV, hits como ''Under the Bridge'' e ''Californication'' são considerados hinos do rock, não só pela perfeição da melodia e letra das músicas, como também pelo clipe e a grande aceitação por parte dos críticos musicais. Fora da musicalidade, a luta contra as drogas do vocalista Anthony Kiedis, que já foi premiado em 2009 por sua luta, influencia muitas pessoas que estão afundadas nesse mundo a saírem dele. No final da década de 80, ele estava praticamente no ''fundo do poço'', mas a sua luta foi maior e a superação fez ele vencer. O ex-guitarrista da banda, o aclamado John Frusciante, nos anos 90, não conseguia viver sem as drogas. Mas com o acolhimento dos membros da banda em sua volta conjunta com sua própria superação, fez com que ele saísse totalmente desse mundo. Podemos dizer que os Peppers são a verdadeira banda que experimentou o mundo do sexo, drogas e rock n' roll em excesso, e conseguiu superá-la com muita garra, por isso é sempre lembrada por isso.
                      A banda já ganhou vários prêmios da indústria musical ao longo de sua carreira. Foram nomeados 16 vezes no Grammy Awards, sendo a primeira indicação para o "Prêmio Grammy para Melhor Performance de Rock por um Duo ou Grupo com Vocais" com "Higher Ground" em 1991, já o primeiro a ser ganho pelo grupo foi a "Melhor Performance de Hard Rock" em 1993 com "Give It Away". Desde então a banda ganhou outros como a "Melhor Canção de Rock" com "Scar Tissue" em 2000 e em 2007 o grupo foi nomeado em 6 categorias, no qual venceram 4: "Melhor Performance de Rock em Dupla ou Grupo com Vocal", "Melhor Canção de Rock" com "Dani California", "Melhor Álbum de Rock" e "Melhor Disco em Edição Especial Limitada" com Stadium Arcadium.[2] Em 2012 receberam uma nomeação na categoria "Melhor Álbum de Rock" com I'm With You.
                      Além disso venceram outros diversos prêmios como "Melhor Banda da atualidade" no Q Awards,[93] duas vezes "Melhor Grupo Internacional" no Echo Music Awards em 2003 e 2007, quatro prêmios "Meteor Ireland Music Awards", o "MTV Video Music Brasil" em 2007 com "Melhor Artista Internacional do Ano" e 8 das 26 indicações ao MTV Video Music Awards entre outros prêmios e reconhecimentos.
                    </p>
