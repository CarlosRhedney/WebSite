                <div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree">
                  <div class="panel-body">
                    <p>
                      Em 13 de abril de 2010, Eminem anunciou via Twitter aos seus fãs que não haveria um Relapse 2. Quando ele havia falado isso, as pessoas acreditaram que não haveria mais o lançamento de um novo álbum, mas ele simplesmente alterou o seu título para Recovery. Ele completou: "Eu tinha planejado originalmente para o Relapse 2 sair ano passado. Mas eu continuei gravando e trabalhando com novos produtores, e então a ideia de uma sequência de 'Relapse' começou a fazer cada vez menos sentido para mim, pois eu queria fazer algo completamente novo. A música em 'Recovery' é muito diferente mesmo de 'Relapse', e ela merecia seu título próprio".
                      Seu sétimo álbum de estúdio, Recovery, foi lançado em 21 de junho. Nos EUA, Recovery vendeu 741.000 cópias na primeira semana e alcançou o topo da Billboard 200. Até novembro de 2010, ele havia vendido 2,9 milhões de cópias nos EUA. Apesar de algumas críticas em relação a sua consistência, Recovery foi bem recebido pela maioria dos profissionais de música, como uma recuperação em relação ao álbum anterior. Seu primeiro single, "Not Afraid" foi lançado em 29 de abril e estreou em primeiro na Billboard Hot 100. O segundo, "Love the Way You Lie" estreou em segundo e depois subiu para a primeira posição. Ele contou com a presença da cantora de R&amp;B Rihanna no refrão e seu videoclipe teve 6,6 milhões de acessos no primeiro dia, um recorde na história do YouTube.
                      Em junho de 2010, Eminem anunciou juntamente com Jay-Z que iria tocar em vários lugares de Detroit e Nova Iorque, em uma turnê chamada de The Home &amp; Home Tour. Os ingressos para os dois primeiros shows se esgotaram rapidamente, fazendo que shows adicionais fossem marcados. Eminem abriu a premiação do Video Music Awards de 2010, com seus singles "Not Afraid" e "Love the Way You Lie", com uma excelente recepção por parte do público. Eminem participou ainda na segunda parte da sequela de "Love the Way You Lie", presente no álbum de estúdio Loud de Rihanna. O rapper ainda fez uma participação no álbum de Nicki Minaj Pink Friday, mais especificamente na canção "Roman's Revenge", que fez referência aos alter ego de ambos: Roman Zolanski e Slim Shady. Em dezembro de 2010, na escolha dos 25 melhores momentos de 2010, feita pela Billboard, The "Great Eminem Recovery" foi colocado como o principal acontecimento da música no ano.
                      No Grammy Awards de 2011, Eminem foi indicado nas categorias de "Álbum do Ano" (com Recovery), "Canção do Ano" e "Gravação do Ano" (ambas com Love the Way You Lie) e "Melhor Álbum de Rap" (também com Recovery), mas saiu vencedor apenas na última.
                    </p>
