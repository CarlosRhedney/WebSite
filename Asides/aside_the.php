        <h3>History</h3>
        <p>Nirvana foi uma banda norte-americana de rock, formada pelo vocalista e guitarrista Kurt Cobain e pelo baixista Krist Novoselic em Aberdeen no ano de 1987.</p>
        <h3>Style</h3>
        <p>Cobain descreveu o som do Nirvana quando ele começou como "uma imitação do Gang of Four e do Scratch Acid". Quando o Nirvana gravou Bleach, Cobain sentiu que ele tinha que se adequar às expectativas do som grunge da Sub Pop para construir uma base de fãs e, portanto, reprimiu suas características de composição artísticas e pop durante a elaboração da gravação a favor de um som mais rock.</p>
        <h3>Formation</h3>
        <p>
          <p>Kurt Cobain – vocal e guitarra (1987–1994)</p>
          <p>Krist Novoselic – baixo (1987–1994)</p>
          <p>Dave Grohl – bateria e backing vocal (1990–1994)</p>
